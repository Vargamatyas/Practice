from tkinter import *

root = Tk()
root.geometry("1300x800")

frame = Frame(root)
frame.pack()


class Labeel:

    def __init__(self, name, row, column, bg):

        self.name = name
        self.row = row
        self.column = column
        self.bg = bg
        yx = Label(frame, text=self.name, font=("H", 10), width=7, height=4, bg=self.bg)
        yx.grid(row=self.row, column=self.column)


class Atom:

    def __init__(self, name, row, column, mass, bg):

        self.name = name
        self.row = row
        self.column = column
        self.mass = str(mass)
        self.bg = bg
        xy = Button(frame, text=self.name, font=("H", 10), width=7, height=4,
                    command=lambda: Atom.command1(self), bg=self.bg)
        xy.grid(row=self.row, column=self.column)

    def command1(self):
        xyz = Button(frame, text=self.mass, font=(self.mass, 10), width=7, height=4,
                     command=lambda: Atom.newlabel(self), bg=self.bg)
        xyz.grid(row=self.row, column=self.column)

    def newlabel(self):
        newlabel = Button(frame, text=self.name, font=(self.name, 10), width=7, height=4,
                          command=lambda: Atom.command1(self), bg=self.bg)
        newlabel.grid(row=self.row, column=self.column)


